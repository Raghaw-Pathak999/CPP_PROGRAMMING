#include <iostream>
using namespace std;

int main()
{
    int number = 10;
    float dNUM = 11.11;
    char firstChar = 'A';
    string fullname = "ANAS";

    cout << "INT --->> " << number << endl;
    cout << "FLOAT --->> " << dNUM << endl;
    cout << "CHAR --->> " << firstChar << endl;
    cout << "FULL NAME --->> " << fullname << endl;
    cout << "INDEX --->> " << fullname[2] << endl;

    long long int dd = 100000000000000;

    cout << dd;

    return 0;
}